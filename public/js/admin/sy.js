function editSy(id, school_year, start_date, end_date, status){
    document.getElementById('edit_sy_id').value = id;
    document.getElementById('edit_school_year').value = school_year;
    document.getElementById('edit_start_date').value = start_date;
    document.getElementById('edit_end_date').value = end_date;
    document.getElementById('edit_status').value = status;
}